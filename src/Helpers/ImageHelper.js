export const getImageByCategoryId = (categoryId) => {
    switch (parseInt(categoryId)) {
        case 1:
            return require('../images/icons/sto.png')
        case 2:
            return require('../images/icons/sto.png')
        case 5:
            return require('../images/icons/allAvto.png')
        case 6:
            return require('../images/icons/carWash.png')
        case 7:
            return require('../images/icons/farmacy.png')
        case 8:
            return require('../images/icons/dryCleaning.png')
        case 9:
            return require('../images/icons/dantists.png')
        case 10:
            return require('../images/icons/rest.png')
        case 11:
            return require('../images/icons/rest.png')
        case 12:
            return require('../images/icons/pub.png')
        case 13:
            return require('../images/icons/education.png')
        case 14:
            return require('../images/icons/beauty.png')
        case 15:
            return require('../images/icons/rest.png')
        case 16:
            return require('../images/icons/mall.png')
        case 17:
            return require('../images/icons/trc.png')
        case 18:
            return require('../images/icons/store-icon.png')
        default:
            return require('../images/icons/sto.png')
    }
}

